#ifndef __ASCII_H__
#define __ASCII_H__

#define NUL	(0x00)	/* NULL */
#define BS	(0x08)	/* Back Space */
#define HT	(0x09)	/* Horizontal Tab */
#define LF	(0x0A)	/* \n : NL Line Feed, New Line*/
#define VT  (0x0B)  /* Vertical Tab */
#define CR	(0x0D)	/* \r : Carriage Return */

#endif /* __ASCII_H__ */
